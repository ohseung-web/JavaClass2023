package classpart08;

public class ClassEx8 {

	int x;
	int y;
	
	public ClassEx8(){}
	public ClassEx8(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public int Plus(int x, int y) {
		return x + y;
	}
	
	public int subtract(int x, int y) {
		return x - y;
	}
	
	public int multiply(int x,int y) {
		return x * y;
	}
	
	public double divide(int x, int y) {
		return x / y;
	}
}
